package es.udc.controller;

import java.time.LocalDate;
import java.util.List;

public class PlanViajeRequestDTO {

    private Long id;
    private String nombre;
    private float precio;
    private String alojamiento;
    private String transporte;
    private List<String> actividades;
    private Long compradorId;
    private List<Long> viajerosId;
    private LocalDate fechaInicio;  // Nuevo campo
    private LocalDate fechaFin;     // Nuevo campo

    // Getters y setters

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public float getPrecio() {
        return precio;
    }

    public void setPrecio(float precio) {
        this.precio = precio;
    }

    public String getAlojamiento() {
        return alojamiento;
    }

    public void setAlojamiento(String alojamiento) {
        this.alojamiento = alojamiento;
    }

    public String getTransporte() {
        return transporte;
    }

    public void setTransporte(String transporte) {
        this.transporte = transporte;
    }

    public List<String> getActividades() {
        return actividades;
    }

    public void setActividades(List<String> actividades) {
        this.actividades = actividades;
    }

    public Long getCompradorId() {
        return compradorId;
    }

    public void setCompradorId(Long compradorId) {
        this.compradorId = compradorId;
    }

    public List<Long> getViajerosId() {
        return viajerosId;
    }

    public void setViajerosId(List<Long> viajerosId) {
        this.viajerosId = viajerosId;
    }

    public LocalDate getFechaInicio() {
        return fechaInicio;
    }

    public void setFechaInicio(LocalDate fechaInicio) {
        this.fechaInicio = fechaInicio;
    }

    public LocalDate getFechaFin() {
        return fechaFin;
    }

    public void setFechaFin(LocalDate fechaFin) {
        this.fechaFin = fechaFin;
    }
}
